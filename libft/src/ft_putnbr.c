#include "../includes/libft.h"

void	ft_putnbr(int n)
{
	char	c;

	c = 0;
	if (n == -2147483648)
		ft_putstr("-2147483648");
	if (n < 0)
	{
		ft_putchar('-');
		n = n * -1;
	}
	if (n > 9)
	{
		ft_putnbr(n / 10);
		n = n % 10;
	}
	c = (n % 10) + '0';
	ft_putchar(c);
}
