#include "../includes/libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char *cs;

	cs = (unsigned char *)s;
	while (n--)
		if (*(cs++) == (unsigned char)c)
			return ((void*)--cs);
	return (NULL);
}
